/**
 * Will get a likey first name
 * @returns A name
 */
export function getFirstName() {
  const rand = new RandomWholeNumber();
  rand.setMinMax(0, firstNames.length - 1);
  return firstNames[+rand.next()];
}
/**
 * will get a likely last name
 * @returns A name
 */
export function getLastName() {
  const rand = new RandomWholeNumber();
  rand.setMinMax(0, lastNames.length - 1);
  return lastNames[+rand.next()];
}
/**
 * will get a likely first and last name
 * @returns A full name "firstName lastName"
 */
export function getName() {
  return `${getFirstName()} ${getLastName()}`;
}

export function getNames(num: number) {
  let arr = [];
  let randFirst = new RandomWholeNumber();
  randFirst.setMinMax(0, firstNames.length - 1);
  let randLast = new RandomWholeNumber();
  randLast.setMinMax(0, lastNames.length);
  while (num) {
    arr.push(
      `${firstNames[+randFirst.next(3456)]} ${lastNames[+randLast.next(7654)]}`
    );
    num--;
  }
  return arr;
}
